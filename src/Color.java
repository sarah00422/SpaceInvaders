
public class Color {
	int R,G,B,A;
 public Color(int R, int G, int B, int A) {
	 this.R = R;
	 this.G = G;
	 this.B = B;
	 this.A = A;
 }
public int getR() {
	return R;
}
public void setR(int r) {
	R = r;
}
public int getG() {
	return G;
}
public void setG(int g) {
	G = g;
}
public int getB() {
	return B;
}
public void setB(int b) {
	B = b;
}
public int getA() {
	return A;
}
public void setA(int a) {
	A = a;
}
 
}
